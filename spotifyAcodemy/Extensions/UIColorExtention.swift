import UIKit

extension UIColor {
    
    static func gold() -> UIColor {
        return UIColor(red: 181/255.0, green: 139/255.0, blue: 89/255.0, alpha: 1)
    }
    
    static func darklyDark() -> UIColor {
        return UIColor(red: 29.0/255.0, green: 29.0/255.0, blue: 29.0/255.0, alpha: 1)
    }
}
